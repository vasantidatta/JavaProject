package basicsofjava;

public class BitwiseOperators {

	public static void main(String[] args) {
		
		int a=2;
		int b=3;
		int c=0;
		
		 c = a&b;
		 
		 System.out.println("a&b = "+c);
		 
		 c=a|b;
		 
		 System.out.println("a|b = "+c);
		 
		 c=a^b;
		 
		 System.out.println("a^b = "+c);

	}

}
